package Variaveis;

public class ParOuImpar {
    public static void main(String[] args) {
        int numero = 1;

        if (numero % 2 == 0) {
            System.out.println("O numero " + numero + " é par") ;
        } else  {
            System.out.println("O numero " + numero + " é impar") ;
        }
    }
}
